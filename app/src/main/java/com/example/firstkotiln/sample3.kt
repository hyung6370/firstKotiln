package com.example.firstkotiln

fun main() {
    var input = readLine()!!.split(" ")
    var a = input[0].toInt()
    var b = input[1].toInt()
    var c = input[2].toInt()
    println(a + b + c)
    println(a - b - c)
    println(a * b * c)
    println(a / b + c)
    println(a % b + c)

    //이것은 사칙연산 입니다!!!
}

