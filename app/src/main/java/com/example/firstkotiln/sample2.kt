package com.example.firstkotiln

fun main() {
    var input = readLine()!!.split(" ")
    var a = input[0].toInt()
    var b = input[1].toInt()
    var c = input[2].toInt()


    println(a+b+c)
    println(a-b+c)
    println(a*b*c)
    println(a%b*c)

    println(a * a)
    println(b * b)

    //이건 샘플2도 아니고 샘플3도 아니고 샘플 2.5라 합시다!

}